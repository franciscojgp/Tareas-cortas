
alert("Si deseas cambiar de grados Celius a Fahrenheit, indica aqui el grado en Celcius.");
var celcius = prompt("Dinos el grado en celcius que quieres que transformemos.")

var fahrenheit = 33.8;

gradoEnFahrenheit = fahrenheit * celcius;
gradoEnFahrenheit = parseInt(gradoEnFahrenheit);
document.write("Correspondería a " + gradoEnFahrenheit +"° Fahrenheit.");


